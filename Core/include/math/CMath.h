#ifndef CMATH_H
#define CMATH_H
//===========================================================================
#include <math.h>

//===========================================================================
inline double cDegToRad(const double& a_angleDeg)
{
    return (a_angleDeg * 3.14159265358979323846264338327950288 / 180);
}

//===========================================================================
inline double cRadToDeg(const double& a_angleRad)
{
    return (a_angleRad * 180 / 3.14159265358979323846264338327950288);
}

//===========================================================================
#endif
